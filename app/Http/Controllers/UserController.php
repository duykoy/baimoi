    {
        $acc=new User();
        $acc->username=$request->get('username');
        $acc->email=$request->get('email');
        $hashedPw = sha1($request->get('password'));
        $salt = str_random(6);
        $acc->password = $hashedPw.$salt;
        $acc->fullname = $request->get('fullname');
        $acc->save();
        return view('dktc');

